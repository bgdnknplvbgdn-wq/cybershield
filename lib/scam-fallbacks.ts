const fallbacksByScenario: Record<ScamScenarioId, string[]> = {
  bank: bankFallbacks,
  email: emailFallbacks,
  intercom: intercomFallbacks,
  classmate: classmateFallbacks,
};

export function getFallbackResponse(
  scenarioId: ScamScenarioId,
  _userMessage: string,
  messageIndex: number
): string {
  const responses = fallbacksByScenario[scenarioId];
  const idx = Math.min(messageIndex, responses.length - 1);
  return responses[idx];
}
